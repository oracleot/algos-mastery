/**
 * Get catalog problems filtered by topic
 * @param topic - The topic slug to filter by
 * @returns Sorted array of catalog problems for that topic
 */
export function getCatalogByTopic(topic: TopicSlug): CatalogProblem[] {
  return PROBLEM_CATALOG.filter((p) => p.topic === topic).sort(
    (a, b) => a.order - b.order
  );
}

/**
 * Get catalog problems filtered by source
 * @param source - The source to filter by
 * @returns Array of catalog problems from that source
 */
export function getCatalogBySource(source: CatalogSource): CatalogProblem[] {
  return PROBLEM_CATALOG.filter((p) => p.source === source);
}

/**
 * Get catalog problems filtered by difficulty
 * @param difficulty - The difficulty to filter by
 * @returns Array of catalog problems with that difficulty
 */
export function getCatalogByDifficulty(
  difficulty: 'easy' | 'medium' | 'hard'
): CatalogProblem[] {
  return PROBLEM_CATALOG.filter((p) => p.difficulty === difficulty);
}

/**
 * Search catalog problems by title
 * @param query - Search query string
 * @returns Matching catalog problems
 */
export function searchCatalog(query: string): CatalogProblem[] {
  const lowerQuery = query.toLowerCase().trim();
  if (!lowerQuery) return PROBLEM_CATALOG;

  return PROBLEM_CATALOG.filter((p) =>
    p.title.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Get the total count of problems by source
 */
export function getCatalogSourceCounts(): Record<CatalogSource, number> {
  return {
    'blind-75': getCatalogBySource('blind-75').length,
    'neetcode-150': getCatalogBySource('neetcode-150').length,
    'grind-75': getCatalogBySource('grind-75').length,
    curated: getCatalogBySource('curated').length,
  };
}
